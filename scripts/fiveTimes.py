print('Primeiro exemplo com for')
print('My name is')
for i in range(5):
    print('Jimmy Five Times (' + str(i) + ')')


print('Segundo exemplo com while')
print('My name is')
i = 0
while i < 5:
    print('Jimmy Five Times (' + str(i) + ')')
    i = i + 1

print('Outra forma de usar o for com a função range')
for i in range(0,10,2):
    print(i)

print('Usando for para decremento')
for i in range(5,-1,-1):
    print(i)
    
